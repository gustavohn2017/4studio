// Funções utilitárias específicas para o tema
export const getColorValue = (colorPath: string): string => {
  const parts = colorPath.split('.');
  let value: any = theme;
  
  for (const part of parts) {
    if (value[part] === undefined) {
      return '';
    }
    value = value[part];
  }
  
  return value;
};
